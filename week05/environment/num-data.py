#!/bin/python

print("Python has three numeric types: int, float, and complex")

myValue=1
print(myValue)  #write or add to shell
print(type(myValue)) # get data type of variable
print(str(myValue) + " is of the data type " + str(type(myValue)))  # combine num and text 

myValue=3.14   # reassign to float
print(myValue)   # write to shell
print(type(myValue))   # get data type of variable
print(str(myValue) + " is of the data type " + str(type(myValue)))  # combine num and text 

myValue=5j   # add complex data type
print(myValue)   # write to shell
print(type(myValue))   # get data type of variable
print(str(myValue) + " is of the data type " + str(type(myValue)))  # combine num and text 

myValue=True   # add boolean data type
print(myValue)   # write to shell
print(type(myValue))   # get data type of variable
print(str(myValue) + " is of the data type " + str(type(myValue)))  # combine num and text

myValue=False   # add another boolean data type
print(myValue)   # write to shell
print(type(myValue))   # get data type of variable
print(str(myValue) + " is of the data type " + str(type(myValue)))  # combine num and text
